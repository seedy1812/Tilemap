bcd_reset:
   ld hl,bcd_number_start
   sub a
   sub a
   ld b, bcd_number_end - bcd_number_start
bcd_reset_loop:
   ld (hl),a
   inc hl
   djnz bcd_reset_loop
   ret

bcd_number_start:   db 0,0,0,0
bcd_number_end: equ *

bcd_add_byte:
    ld b,0
    and a
    ld hl , bcd_number_end-1
    add a,(hl)
 bcd_add_loop:
    daa
    ld (hl),a
    ret nc
    dec hl
    ld a,b
    adc a,(hl)  
    daa
    ld (hl),a
    ret nc
    dec hl
    ld a,b
    adc a,(hl)
    daa
    ld (hl),a
    ret nc
    dec hl
    ld a,b
    adc a,(hl)
    daa
    ld (hl),a
    ret


